"""
Operadores aritiméticos
+ Soma
- Subtração
* Multiplicação
/ Divisão
// Divisão inteira
** Potenciação
% Módulo-resto da divisão
() Altera a precedência
"""
print('Multiplicação *', 10 * 10)
print('Adição +', 10 + 10)
print('Eliezer ' + '' + 'Lucas')  # concatenação
print(10 * 'Eliezer')
print('Subtração - ', 10 - 5)
print('Divisão /', 10 / 3)
print('Divisão inteira', 10 // 3)
print('Potenciação', 2 ** 10)
print('Módulo', 10 % 3)
print(5 + 2 * 10)
print((5 + 2) * 10)


